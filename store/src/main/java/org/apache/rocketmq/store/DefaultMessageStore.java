    /**
     *
     * @param group Consumer group that launches this query. 所属消费者组
     * @param topic Topic to query. 主体名称
     * @param queueId Queue ID to query. 查询的队列id
     * @param offset Logical offset to start from. 起始逻辑偏移量
     * @param maxMsgNums Maximum count of messages to query. 要查询的最大消息数，默认32，消费者处传递
     * @param messageFilter Message filter used to screen desired messages. 用于筛选所需消息的消息过滤器
     * @return
     */
    public GetMessageResult getMessage(final String group, final String topic, final int queueId, final long offset,
        final int maxMsgNums,
        final MessageFilter messageFilter) {
        //前置校验
        if (this.shutdown) {
            log.warn("message store has shutdown, so getMessage is forbidden");
            return null;
        }

        if (!this.runningFlags.isReadable()) {
            log.warn("message store is not readable, so getMessage is forbidden " + this.runningFlags.getFlagBits());
            return null;
        }

        if (MixAll.isLmq(topic) && this.isLmqConsumeQueueNumExceeded()) {
            log.warn("message store is not available, broker config enableLmq and enableMultiDispatch, lmq consumeQueue num exceed maxLmqConsumeQueueNum config num");
            return null;
        }

        //起始时间
        long beginTime = this.getSystemClock().now();

        //拉取消息的状态
        GetMessageStatus status = GetMessageStatus.NO_MESSAGE_IN_QUEUE;
        //下次拉取的consumeQueue的起始逻辑偏移量
        long nextBeginOffset = offset;
        //最小偏移量
        long minOffset = 0;
        //最大偏移量
        long maxOffset = 0;

        // lazy init when find msg.
        GetMessageResult getResult = null;

        //获取mappedFileQueue内的的最大物理偏移量
        final long maxOffsetPy = this.commitLog.getMaxOffset();

        //找到与topic和queueId匹配的消费者队列
        ConsumeQueue consumeQueue = findConsumeQueue(topic, queueId);
        if (consumeQueue != null) {
            //consumeQueue的最小偏移量
            minOffset = consumeQueue.getMinOffsetInQueue();
            //最大偏移量
            maxOffset = consumeQueue.getMaxOffsetInQueue();

            if (maxOffset == 0) {
                //最大偏移量为0
                //表示消息队列无消息，设置NO_MESSAGE_IN_QUEUE
                status = GetMessageStatus.NO_MESSAGE_IN_QUEUE;
                //矫正下一次拉取的开始偏移量，如果broker不是SLAVE节点，或者是SLAVE节点但是从服务器支持offset检查，则为0
                nextBeginOffset = nextOffsetCorrection(offset, 0);
            } else if (offset < minOffset) {
                //consumer传递的offset小于最小偏移量，表示拉取的位置太小，设置OFFSET_TOO_SMALL
                status = GetMessageStatus.OFFSET_TOO_SMALL;
                //矫正下一次拉取的开始偏移量为最小偏移量
                nextBeginOffset = nextOffsetCorrection(offset, minOffset);
            } else if (offset == maxOffset) {
                //consumer传递的offset等于最大偏移量，表示拉取的位置溢出，设置OFFSET_OVERFLOW_ONE
                status = GetMessageStatus.OFFSET_OVERFLOW_ONE;
                //矫正下一次拉取的开始偏移量，还是offset
                nextBeginOffset = nextOffsetCorrection(offset, offset);
            } else if (offset > maxOffset) {
                //consumer传递的offset大于最大偏移量，表示拉取的位置严重溢出，设置OFFSET_OVERFLOW_BADLY
                status = GetMessageStatus.OFFSET_OVERFLOW_BADLY;
                if (0 == minOffset) {
                    //最小偏移量为0，置为0
                    nextBeginOffset = nextOffsetCorrection(offset, minOffset);
                } else {
                    //置为最大值
                    nextBeginOffset = nextOffsetCorrection(offset, maxOffset);
                }
            }
            //consumer传递的offset大于等于minOffset，且小于maxOffset，表示偏移量在正常范围内
            else {
                /*
                 * 根据逻辑offset定位到物理偏移量，然后截取该偏移量之后的一段Buffer，
                 * 其包含要拉取的消息的索引数据及对应consumeQueue文件内的mappedFile物理偏移量之后的mappedFile内的全部索引数据。
                 */
                SelectMappedBufferResult bufferConsumeQueue = consumeQueue.getIndexBuffer(offset);
                if (bufferConsumeQueue != null) {
                    //索引数据不为空
                    try {
                        status = GetMessageStatus.NO_MATCHED_MESSAGE;

                        //下一个commitLog文件的起始物理偏移量，默认从Long.MIN_VALUE开始
                        //用来记录上一次循环的时候时候是否换到了下一个commitLog文件
                        long nextPhyFileStartOffset = Long.MIN_VALUE;
                        //本次消息拉取的最大物理偏移量
                        long maxPhyOffsetPulling = 0;

                        int i = 0;
                        //每次最大的过滤消息字节数，一般为16000KB
                        //转换成条数：16000 / 20 = 800条，20kb为CQ_STORE_UNIT_SIZE
                        final int maxFilterMessageCount = Math.max(16000, maxMsgNums * ConsumeQueue.CQ_STORE_UNIT_SIZE);
                        //是否需要记录commitLog磁盘的剩余可拉取的消息字节数，默认true
                        final boolean diskFallRecorded = this.messageStoreConfig.isDiskFallRecorded();

                        //创建拉取结果对象
                        getResult = new GetMessageResult(maxMsgNums);

                        //存储单元
                        ConsumeQueueExt.CqExtUnit cqExtUnit = new ConsumeQueueExt.CqExtUnit();
                        //循环遍历截取的buffer，处理每一条ConsumeQueue索引，拉取消息，
                        // ConsumeQueue消息CQ_STORE_UNIT_SIZE固定长度20字节，因此每次移动20B的长度
                        for (; i < bufferConsumeQueue.getSize() && i < maxFilterMessageCount; i += ConsumeQueue.CQ_STORE_UNIT_SIZE) {
                            //获取CQ_STORE_UNIT_SIZE的0-7（8）字节数据，也就是消息在CommitLog中的物理偏移量
                            long offsetPy = bufferConsumeQueue.getByteBuffer().getLong();
                            //获取CQ_STORE_UNIT_SIZE的8-11（4）字节数据，消息的大小
                            int sizePy = bufferConsumeQueue.getByteBuffer().getInt();
                            //获取CQ_STORE_UNIT_SIZE的12-19（8）字节数据,延迟消息就是消息投递时间，其他消息就是消息的tags的hashCode，即生产者发送消息时设置的tags
                            //消费数据时，可以通过对比消费者设置的过滤信息来匹配消息
                            long tagsCode = bufferConsumeQueue.getByteBuffer().getLong();

                            //更新maxPhyOffsetPulling为当前消息在CommitLog中的物理偏移量
                            maxPhyOffsetPulling = offsetPy;

                            //如果nextPhyFileStartOffset不为Long.MIN_VALUE，并且offsetPy 小于 nextPhyFileStartOffset那
                            //表示切换到了下一个commitLog文件，并且当前偏移量 < 下一该文件最小偏移量，那么跳过该消息的处理
                            if (nextPhyFileStartOffset != Long.MIN_VALUE) {
                                if (offsetPy < nextPhyFileStartOffset)
                                    continue;
                            }

                            //检查要拉取的消息是否在磁盘上
                            boolean isInDisk = checkInDiskByCommitOffset(offsetPy, maxOffsetPy);

                            //判断消息拉取是否达到上限，如果达到上限，则跳出循环，结束消息的拉取
                            if (this.isTheBatchFull(sizePy, maxMsgNums, getResult.getBufferTotalSize(), getResult.getMessageCount(),
                                isInDisk)) {
                                break;
                            }

                            //扩展存储信息判断
                            boolean extRet = false, isTagsCodeLegal = true;
                            if (consumeQueue.isExtAddr(tagsCode)) {
                                extRet = consumeQueue.getExt(tagsCode, cqExtUnit);
                                if (extRet) {
                                    tagsCode = cqExtUnit.getTagsCode();
                                } else {
                                    // can't find ext content.Client will filter messages by tag also.
                                    log.error("[BUG] can't find consume queue extend file content!addr={}, offsetPy={}, sizePy={}, topic={}, group={}",
                                        tagsCode, offsetPy, sizePy, topic, group);
                                    isTagsCodeLegal = false;
                                }
                            }

                            /*
                             * Broker内的TAG过滤
                             * broker获取对应ConsumerQueue里hashcode(tag)后根据消费端传入的tag进行比较，
                             * 如果不匹配则将此消息跳过；如果匹配消费端还要进行一次tag的比较，因为会有可能出现了hash冲突
                             */
                            if (messageFilter != null
                                && !messageFilter.isMatchedByConsumeQueue(isTagsCodeLegal ? tagsCode : null, extRet ? cqExtUnit : null)) {
                                if (getResult.getBufferTotalSize() == 0) {
                                    //如果过滤没通过，并且已拉取的消息总大小为0，则设置为NO_MATCHED_MESSAGE状态
                                    status = GetMessageStatus.NO_MATCHED_MESSAGE;
                                }

                                continue;
                            }

                            //TAG校验通过，调用commitLog#getMessage方法根据消息的物理偏移量和消息大小获取该索引对应的真正的消息
                            SelectMappedBufferResult selectResult = this.commitLog.getMessage(offsetPy, sizePy);
                            if (null == selectResult) {
                                //没找到消息，该偏移量可能到达了文件末尾，消息存放在下一个commitLog文件中
                                if (getResult.getBufferTotalSize() == 0) {
                                    status = GetMessageStatus.MESSAGE_WAS_REMOVING;
                                }

                                //nextPhyFileStartOffset设置为下一个commitLog文件的起始物理偏移量，并跳过本次拉取
                                nextPhyFileStartOffset = this.commitLog.rollNextFile(offsetPy);
                                continue;
                            }
                            /*
                             * 找到了消息，继续通过messageFilter#isMatchedByCommitLog方法执行消息SQL92 过滤
                             * SQL92 过滤依赖于消息中的属性，而消息体的内容存放在commitLog中的，因此需要先拉取到消息，在进行SQL92过滤
                             */
                            if (messageFilter != null
                                && !messageFilter.isMatchedByCommitLog(selectResult.getByteBuffer().slice(), null)) {
                                if (getResult.getBufferTotalSize() == 0) {
                                    status = GetMessageStatus.NO_MATCHED_MESSAGE;
                                }
                                // release...
                                //过滤不通过，释放这一段内存，并跳过本次拉取
                                selectResult.release();
                                continue;
                            }

                            //传输的单条消息数量自增1，用于展示
                            this.storeStatsService.getGetMessageTransferedMsgCount().add(1);
                            //TAG和SQL92校验通过，那么将当前消息存入getResult
                            getResult.addMessage(selectResult);
                            //更改status
                            status = GetMessageStatus.FOUND;
                            //nextPhyFileStartOffset重新置为Long.MIN_VALUE，继续下一次循环
                            nextPhyFileStartOffset = Long.MIN_VALUE;
                        }

                        //需要记录commitLog磁盘的剩余可拉取的消息字节数
                        if (diskFallRecorded) {
                            //当前mappedFileQueue中剩余可拉取的消息字节数 = 当前最大物理偏移量 - 本次消息拉取的最大物理偏移量
                            long fallBehind = maxOffsetPy - maxPhyOffsetPulling;
                            //记录剩余的commitLog磁盘可拉取的消息字节数
                            brokerStatsManager.recordDiskFallBehindSize(group, topic, queueId, fallBehind);
                        }
                        //计算下一次读取数据的ConsumeQueue的开始偏移量，判断是否建议下一次从SLAVE broker中拉取消息
                        //计算下一次读取数据的ConsumeQueue的开始偏移量
                        nextBeginOffset = offset + (i / ConsumeQueue.CQ_STORE_UNIT_SIZE);

                        //当前mappedFileQueue中剩余可拉取的消息字节数
                        long diff = maxOffsetPy - maxPhyOffsetPulling;
                        //broker服务最大可使用物理内存
                        long memory = (long) (StoreUtil.TOTAL_PHYSICAL_MEMORY_SIZE
                            * (this.messageStoreConfig.getAccessMessageInMemoryMaxRatio() / 100.0));
                        //如果剩余的commitLog磁盘可拉取的消息字节数 > broker服务最大可使用物理内存，
                        //那么设置建议下一次从SLAVE broker中拉取消息
                        getResult.setSuggestPullingFromSlave(diff > memory);
                    } finally {

                        //buffer内存释放
                        bufferConsumeQueue.release();
                    }
                } else {
                    //没获取到缓存buffer，可能是到达了当前consumeQueue文件的尾部
                    status = GetMessageStatus.OFFSET_FOUND_NULL;
                    //nextBeginOffset设置为consumeQueue的下一个文件的起始偏移量
                    nextBeginOffset = nextOffsetCorrection(offset, consumeQueue.rollNextFile(offset));
                    log.warn("consumer request topic: " + topic + "offset: " + offset + " minOffset: " + minOffset + " maxOffset: "
                        + maxOffset + ", but access logic queue failed.");
                }
            }
        } else {
            //没找到consumeQueue
            status = GetMessageStatus.NO_MATCHED_LOGIC_QUEUE;
            //nextBeginOffset设置为0
            nextBeginOffset = nextOffsetCorrection(offset, 0);
        }

        if (GetMessageStatus.FOUND == status) {
            //找到了消息，那么拉取到的次数统计字段getMessageTimesTotalFound+1，用于控制台展示
            //broker中的tps只计算拉取次数，而非拉取的消息条数，默认情况下pushConsumer一次拉取32条
            this.storeStatsService.getGetMessageTimesTotalFound().add(1);
        } else {
            //未找到消息，那么未拉取到的次数统计字段getMessageTimesTotalMiss+1，用于控制台展示
            this.storeStatsService.getGetMessageTimesTotalMiss().add(1);
        }
        //计算本次拉取消耗的时间
        long elapsedTime = this.getSystemClock().now() - beginTime;
        //尝试比较并更新最长的拉取消息的时间字段getMessageEntireTimeMax
        this.storeStatsService.setGetMessageEntireTimeMax(elapsedTime);

        // lazy init no data found.
        if (getResult == null) {
            //没找到消息的情况，延迟初始化GetMessageResult，设置拉取结果为0
            getResult = new GetMessageResult(0);
        }

        //getResult赋值
        getResult.setStatus(status);
        getResult.setNextBeginOffset(nextBeginOffset);
        getResult.setMaxOffset(maxOffset);
        getResult.setMinOffset(minOffset);
        return getResult;
    }

